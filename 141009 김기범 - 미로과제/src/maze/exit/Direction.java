package maze.exit;

public class Direction {
	
	int vert;
	int horiz;
	
	/* Getter & Setter */
	public int getVert() {
		return vert;
	}
	public void setVert(int vert) {
		this.vert = vert;
	}
	public int getHoriz() {
		return horiz;
	}
	public void setHoriz(int horiz) {
		this.horiz = horiz;
	}
		
}
